public class User {

    String name;
    String email;
    String role;

    public void setName(String name) {
        this.name = name;
    }

    ;

    public void setEmail(String email) {
        this.email = email;
    }

    ;

    public void setRole(String role) {
        this.role = role;
    }

    ;

    public String getName() {
        return name;
    }

    ;

    public String getEmail() {
        return email;
    }

    ;

    public String getRole() {
        return role;
    }
;
}
